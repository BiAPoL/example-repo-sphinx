# -*- coding: utf-8 -*-


def add(a: float, b: float):
    """
    Add two floats.

    This is a very nice function that performs the following operation

    .. math:: c = a + b

    Parameters
    ----------
    a : float
        First parameter
    b : float
        Second parameter

    Returns
    -------
    c : float
        This value is returned.

    """
    c = a + b

    return c


def subtract(a: float, b: float) -> float:
    """
    Subtract two floats.

    This is a very nice function that performs the following operation

    .. math:: c = a - b

    Parameters
    ----------
    a : float
        First parameter
    b : float
        Second parameter

    Returns
    -------
    float
        This value is returned.

    """
    c = a - b

    return c
